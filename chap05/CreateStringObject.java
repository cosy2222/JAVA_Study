package chap05;

public class CreateStringObject {

	public static void main(String[] args) {
		
		//1. String 객체 생성 1 : new 키를 사용해서 객체 생성 : Heap 영역의 별도의 공간에 저장
				// RAM의 값을 절대 공유하지 않음!
		String str1= new String("안녕");     // new 키는 객체를 생성하라 (Heap 메모리에 저장)
		System.out.println(str1);           // String 객체를 출력했을떄 객체의 값이 출력
											// String 만 가지는 특별한 기능
											// 객체 자체를 출력 하면, Heap 메모리의 번지
											// 객체의 값을 출력할떄는 객체면.toString() 재정의
		
		String str11 = new String("안녕");    // str1 과 str11 는 별도의 메모리 공간에 저장 (Heap)
		
		if(str1 == str11) {
			System.out.println(true);
		}
		else {
			System.out.println(false);
		}
		
		//2. 리터럴 값을 바로 할당 : 같은 값일경우 RAM 의 값을 공유 하게 된다. (Heap)
		String str2= "안녕하세요." ;
		String str22= "안녕하세요.";    // 동일한 메모리 공간을 공유 (Heap)
		
		if (str2 == str22) {
			System.out.println(true);
		}
		else {
			System.out.println(false);
		}

	}

}
